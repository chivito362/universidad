    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         try {
            Conexion conexion=new Conexion("universidadulp");
            Connection conn=conexion.conn();
            
            
            String sql="SELECT * from alumno where dni="+txtDni.getText()+"";
            PreparedStatement stm=conn.prepareStatement(sql);
            ResultSet resultado=stm.executeQuery();
            
            if(resultado.next()){
                txtNombre.setText(resultado.getString("nombre"));
                txtApellido.setText(resultado.getString("apellido"));
                jcalendario.setDate(resultado.getDate("fechaNacimiento"));
                if(resultado.getInt("estado")==1){
                    cbEstado.setSelectedItem("Activo");
                }else{
                    cbEstado.setSelectedItem("Inactivo");
                }
            }else{
                JOptionPane.showMessageDialog(null, "No se a encontrado ningun alumno con ese DNI");
            }
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "error al realizar la consulta");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String nombre, apellido, dni, fecha, estado;
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        dni = txtDni.getText();
        if (cbEstado.getSelectedItem().toString().equalsIgnoreCase("activo")) {
            estado = "1";
        } else {
            estado = "0";
        }
        fecha = formatoFecha();
        int respuesta = JOptionPane.showConfirmDialog(null, "esta a punto de realizar un cambio en una base de datos desea continuar?");
        if (respuesta == 0) {
            try {
                Conexion conexion = new Conexion("universidadulp");
                Connection conn = conexion.conn();

                String sql = "UPDATE alumno SET apellido='" + apellido + "',nombre='" + nombre + "',fechaNacimiento='" + fecha + "',estado=" + estado + " where dni=" + dni + "";
                PreparedStatement stm = conn.prepareStatement(sql);
                stm.executeUpdate();
                JOptionPane.showMessageDialog(null, "El alumno a sido actualizado");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "error al realizar la consulta");
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed


