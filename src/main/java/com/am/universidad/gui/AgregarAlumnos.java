    private String formatoFecha(){
        if(jcalendario.getDate()!=null){
            LocalDate fecha=jcalendario.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            return fecha.toString();
        }
        return null;
    }
